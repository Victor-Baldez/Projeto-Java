    private void btnCadastrarJogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarJogoActionPerformed
        // TODO add your handling code here:
        if(txtNome.getText().isEmpty() || txtPreco.getText().isEmpty() || txtData.getText().isEmpty() || txtDesenvolvedora.getText().isEmpty() || txtCategoria.getText().isEmpty()){
             JOptionPane.showMessageDialog(null,"Preencha todos os campos!");
        }else{
            Jogo jogo = new Jogo();
            jogo.cadastrarJogo(txtNome.getText(),Double.parseDouble( txtPreco.getText()),LocalDate.parse(txtData.getText()),txtDesenvolvedora.getText(), txtCategoria.getText());
            Jogo.listaJogos.add(jogo);
        }
    }//GEN-LAST:event_btnCadastrarJogoActionPerformed

    
